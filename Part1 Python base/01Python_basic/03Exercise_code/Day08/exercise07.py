"""
    函数实参
"""


def fun01(a, b, c):
    print(a)
    print(b)
    print(c)


# 1.位置实参，根据位置与形参对应
# positional argument
# fun01(1, 2, 3)

# 2.序列实参：星号 *  将序列拆开后与形参的位置依次对应，属于位置实参
# 序列：字符串  列表  元组
# 用途：要传递的参数很多且有序时
list01 = ["a", "b", "c"]
# fun01(*list01)

str01 = "abc"
# fun01(*str01)

# 3.关键字实参，根据形参的名字进行对应
# 用途：
# fun01(b=2, a=1, c="abc")

# 4.字典关键字实参，使用双星号 ** 将字典拆解后与形参的名字对应
# 键是形参的名字，传递值
# 用途：要传递的参数很多且无序时
dic01 = {"c": 33, "a": 11, "b": 22}
fun01(**dic01)
